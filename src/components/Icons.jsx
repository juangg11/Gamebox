export function StarRating({ rating, className = '' }) {
  const value = Number(rating) || 0;
  return (
    <div className={`star-rating-container ${className}`}>
      {[...Array(5)].map((_, i) => {
        const filled = i < Math.floor(value);
        const half = !filled && i < value;
        return (
          <StarIcon
            key={i}
            className={filled ? 'filled' : half ? 'half' : ''}
            width={18}
            height={18}
          />
        );
      })}
    </div>
  );
}

